                <h2 class="title-pattern1 mt-30 mb-30"><span class="title-pattern title-pattern-color mt-0 mb-30">2.2</span> <span>Policy Objectives</h2>
                <ol class="ordered-list-padding" type="A">
                    <li>To create a vibrant ecosystem in the state for the development of human resources and industrial ventures.</li>
                    <li>To promote education and research & development in biotechnology and allied areas.</li>
                    <li>To promote employment generation through application of biotechnology for enhancement of production and growth in agriculture and allied sectors.</li>
                    <li>To encourage biotech industries in the state through establishment of commercial biotechnology ventures.</li>
                    <li>To support biotechnology business for wealth creation and enhance the contribution of biotechnology sector to state's economy substantially.</li>
                    <li>To develop high quality infrastructure with the required support services for manufacturing units through state of art biotechnology parks.</li>
                    <li>To leverage the existing biological (Agriculture, horticulture, forest, animal etc.) and natural (Oil, coal, etc.) resources through use of biotechnology for socio economic development of the state</li>
                </ol>
                <h2 class="title-pattern1 mt-30 mb-30"><span class="title-pattern title-pattern-color mt-0 mb-30">2.2.1</span> <span>Definitions of Biotechnology Unit & Related Terminologies</h2>
                <ol class="ordered-list-padding" type="A">
                    <li><b>Biotechnology unit/company :</b> A unit engaged in manufacture of 'Biotechnology products' (as defined in 2 herein after). The term 'unit' refers to a legal / corporate entity (e.g. a Public or Pvt. Limited Company/ Partnership or Proprietorship Firm) with separate books of accounts and an independently located facility.</li>
                    <li><b>Biotechnology Product :</b> A product, which uses or is derived by using (i) Living systems, (ii) Enzymes, (iii) Bio-catalysts and any of the following processes viz., (a) Genetic Engineering, (b) Cell Culture (c) Bioprocess engineering, (d) Protein Engineering, (e) Tissue Engineering, (f ) Microbial technology, (g) Bio-Informatics, (h) Genomics, (i) Bio-Transformation, (j) Molecular breeding, (k) Bio-Diagnostics, (l) Vaccine Technology, (m) Embryo Manipulations (subject to legal and ethical regulations), (n) Pharmacogenomics, (o) Proteomics or other cutting edge technologies and services included under broad discipline of biotechnology.</li>
                    <li><b>High-end Biotechnology :</b> The technology that involves development of recombinant products, their refinement and modification, would be treated as high- end biotechnology. The high-end biotechnology would also take account of gene technology, animal cloning, nano-biotechnology, pharmaco-genomics, pharmaco-kinetics, new drug delivery mechanism, neuroscience etc. The definition of high-end Biotechnology would be
                    subject to revision and modification from time to time on the basis of the developmental pace of biotechnology</li>
                    <li><b>Existing Biotechnology unit :</b> A Biotechnology unit existing as a legal entity before the date of this policy.</li>
                    <li><b>New Biotechnology unit :</b> A Biotechnology unit formed on or after the date of adoption of this policy. In case of any dispute arising out of interpretation of any definition, the interpretation of state Government shall be treated as final.</li>
                    <li><b>Micro, Small and Medium Enterprises (MSME) :</b> MSME in Biotechnology Industry shall be construed as per the definition under the MSME Act 2006 (as amended from time to time) of the Government of India.</li>
                    <li><b>Gross Fixed Capital Investment (GFCI) :</b> Gross Fixed Capital Investment (GFCI) for applicant units, means the investment made in building, plant and machinery and other related fixed assets including common infrastructure such as effluent treatment plant, testing facilities, shared data centre, excluding land and buildings, required to manufacture end products or provide services by eligible unit for setting up new unit or for undergoing expansion/diversification up to within one year from the date of production or till one year from coming to an end of this Policy, whichever is be capped at 20% of total GFCI. The GFCI for parks will include investment in all facilities listed for the unit, except the building for any of the units if that is not a shared facility. For all units as well as parks and other category of applicants, the cost of land will not be included in computation of GFCI.</li>
                    <li><b>Biotechnology Parks : </b> Biotechnology Parks are facilities created mainly to promote tiny, small and medium biotech entrepreneurs. The Biotechnology Parks would provided a good template for the promotion of Biotech start up companies and the promotion of Public Private Partnerships.</li>
                    <li><b>Horizontal Biotechnology Parks :</b> Horizontal Biotechnology Park means a site which is developed during the operative period of this Policy in minimum area of 10 Acres of which 70% of total saleable/leasable area is allotted to at least five Biotechnology units and minimum 60% of such area assigned for BT units is actually occupied with operational BT units within one year from coming to an end of this Policy.</li>
                    <li><b>Vertical Biotechnology Parks :</b> Vertical Biotechnology Park means new development undertaken during the operative period of Policy in the nature of either a vertical development in the shape of a tower or a group of towers with minimum 1,00,000 sq. ft. of saleable/leasable area out of which 70% of saleable/leasable area is allotted or to be allotted to at least three Biotechnology or IT units; or a vertical development in the shape of a tower or a group of towers with minimum 3000 seat capacity built by any Biotechnology unit/IT unit of which 60% capacity is actually occupied within one year from coming to an end of this Policy.</li>
                    <li><b>Biotech Start-up : </b> An entity involved in crafting repeatable and scalable product or service making a positive impact in the society, where the solution is not obvious and success is not guaranteed. An entity shall be considered as a “Startup” if the entity is recognized as a “Startup” under the “Startup India” Initiative or fulfils all of the following conditions :</li>
                    <ol type="a" class="ordered-list-padding">
                        <li>The date of the entity's incorporation / registration is not prior to seven years. In case an entity is engaged in the biotech sector, the date of incorporation /registration should not be prior to ten years;</li>
                        <li>Its annual turnover does not exceed Rs. 25 crore, for any preceding financial year;</li>
                        <li>It is registered in Assam or employs at least 50 percent of its total qualified workforce from Assam; Such entity is not an extension of an existing family business; or formed by splitting up or reconstruction of a business already in existence.</li>
                    </ol>
                    <li><b>Biotechnology Incubator : </b> Biotechnology Incubator for the purposes of this Policy means an organization registered as an incubator, engaged in accelerating the growth of start-ups through host of support services and resources such as space,
                    funding support, mentoring, market linkages and business management services, which incubates minimum 10 incubatees at a time and out of which minimum 60% are from Biotechnology sector. For new incubators being setup under this Policy, the incubator should get 06 incubatees within 2 years from the date of application out
                    of which minimum 60% will be from Biotechnology sector to be eligible for this assistance.</li>
                </ol>
                <h2 class="title-pattern1 mt-30 mb-30"><span class="title-pattern title-pattern-color mt-0 mb-30">2.2.2</span> <span>Promoting Hign-End Biotechnology Industry in Assam</h2>
                <p>The state has a vision of developing the capabilities for cutting edge technologies which would encourage wealth creation and help in acquiring global strengths. While low-end and conventional biotechnologies have their own relevance and potential, in terms of development of high-end biotechnology. High-end Biotechnology, is highly capital intensive with long gestation period, needs high research inputs and special infrastructure. Therefore, the state would encourage R&D efforts in high end biotechnology.</p>
                <h2 class="title-pattern1 mt-30 mb-30"><span class="title-pattern title-pattern-color mt-0 mb-30">2.2.3</span> <span>Entitlement of Benefits to Biotechnology Industries</h2>
                <p>The Biotechnology industries, as per the eligibility conditions, would be entitled to the benefits, available as per the provisions of the various schemes, framed under this policy. They shall also be eligible for any relevant benefits under other policies of state government namely the industrial policy, the state agriculture policy (for agri-bio units), the state information Technology policy (for bioinformatics units) or any other relevant policy in force, subject to the condition that there shall not be any duplicity of concessions extended by the state government. Any unit which receives any benefit from the state government under any policy shall in a transparent manner, mandatorily submit the necessary details of the benefits received, to the executive committee.</p>
                <h2 class="title-pattern1 mt-30 mb-30"><span class="title-pattern title-pattern-color mt-0 mb-30">2.2.4</span> <span>Registration of Biotechnology Industries or Units</h2>
                <p>The biotechnology industries/companies/units engaged in the manufacture of biotechnology products need to register their facility with the Department of Science & Technology (DST), Government of Assam. The DST would provide a prescribed format of application for registration and will issue the Registration Certificate. The industries /companies/ units shall be able to claim any incentives or relaxations/concessions only on production of such a certificate.</p>
                <h2 class="title-pattern1 mt-30 mb-30"><span class="title-pattern title-pattern-color mt-0 mb-30">2.3</span> <span>Strategy</h2>
                <p>The following key strategies have been identified to address Policy objectives:</p>
                <h2 class="title-pattern1 mt-30 mb-30"><span class="title-pattern title-pattern-color mt-0 mb-30">2.3.1</span> <span>Academic-Industry Linkage</h2>
                <p>The state would facilitate to produce highly skilled manpower in Biotechnology according to industry requirement, by giving financial and technical assistance to colleges and Universities in Assam for industry oriented curriculum development in undergraduate and post graduate level, for promoting research and innovation as well as skill development.</p>
                <h2 class="title-pattern1 mt-30 mb-30"><span class="title-pattern title-pattern-color mt-0 mb-30">2.3.2</span> <span>Process simplification for industry / single window clearance</h2>
                <p>The state would endeavor to create a congenial environment for Biotech Industry through process simplifications to enable clearing of feasible commercial biotechnology venture proposals. In this regard, it is worth mentioning that the state has already launched the ease of doing business reforms.</p>
                <h2 class="title-pattern1 mt-30 mb-30"><span class="title-pattern title-pattern-color mt-0 mb-30">2.3.3</span> <span>Infrastructure for Biotech Process & Product Development</h2>
                <p>The state would set up modern state of art infrastructure for biotechnology process and product development, in the form of Biotechnology parks, research facilities and common equipment centers. The state shall also create, start-up cells and incubation centers at the district science centers which are already functioning and owned by the Science and Technology Department.</p>
                <h2 class="title-pattern1 mt-30 mb-30"><span class="title-pattern title-pattern-color mt-0 mb-30">2.3.4</span> <span>Facilitating Entrepreneurship</h2>
                <p>The state would facilitate entrepreneurship in biotechnology by providing support in technology transfer, business management, capacity building, financial and fiscal incentives.</p>
