        <div class="container pb-40">
          <div class="section-content">
            <div class="row">
              <div class="col-md-12">
                <h2 class="title-pattern1 mt-30 mb-30"><img src="./icons/planting.png" alt="" width="24"> Thrust Areas</h2>
                <p>The following thrust areas have been identified for biotech development in the state of Assam :</p>
                <ol class="ordered-list-padding" type="A">
                    <li><strong>Agriculture biotechnology :</strong> The key activities in agriculture biotechnology would include
                        <ol type="a" class="ordered-list-padding">
                            <li>Development of transgenic crops resistant to biotic and abiotic stress;</li>
                            <li>Molecular breeding and production of genetically uniform quality seeds,</li>
                            <li>Developing Bio- fertilizers, Bio-pesticides, Bio-fuel and value added products,</li>
                            <li>Improvement of Banana, Citrus, Bamboo and Jute germplasm,</li>
                            <li>Tissue culture and bioprospecting of medicinal and aromatic plants,</li>
                            <li>Diagnostics for phyto-pathogen and</li>
                            <li>Tea biotechnology.</li>
                        </ol>
                    </li>
                    <li><strong>Animal biotechnology :</strong> The key activities in animal biotechnology would include
                        <ol type="a" class="ordered-list-padding">
                            <li>Increasing the production, availability and shelf life of animal feed and fodder,</li>
                            <li>Development of nutritionally rich, high protein fish,</li>
                            <li>Increasing productivity of livestock through biotechnological interventions like in vitro fertilization, embryo transfer technology, gene technology etc,</li>
                            <li>Improving the health of productive animals through biotechnology
                                based disease control mechanism and tools;</li>
                            <li>Developing value added animal products,</li>
                            <li>Developing better breeds/strains of livestock and poultry using known or new methods of biotechnology,</li>
                            <li>Developing better strains of livestock species using known or new methods of biotechnology (transgenic cattle),</li>
                            <li>Developing diagnostics, vaccines and therapeutics for major live stock and poultry diseases such as Foot and mouth disease, rabies, hemorrhagic septicemia, anthrax, classical swine fever, avian influenza, tuberculosis, brucellosis, salmonellosis, etc.</li>
                            <li>Establishment of cell lines and semen banking facilities for maintenance and propagation 12 of superior quality germplasm,</li>
                            <li>Development of better methods for pisciculture to cultivate commercially
                                important fish species</li>
                            <li>Genetic improvement of indigenous high-yielding fish species and establishment of live gene bank of indigenous fish species.</li>
                        </ol>
                    </li>
                    <li><strong>Pharmaceutical biotechnology :</strong> The key activities in pharmaceutical biotechnology would include
                        <ol type="a" class="ordered-list-padding">
                            <li>Design and delivery of new therapeutic drugs,</li>
                            <li>Development of diagnostic agents for medical tests,</li>
                            <li>Development of bio-formulation such as antibodies, nucleic acid products and vaccines,</li>
                            <li>Production of drug-precursor and active pharmaceutical ingredients (API),</li>
                            <li>Bio-similars/proteins.</li>
                        </ol>
                    </li>
                    <li><strong>Traditional medicine and herbal technology :</strong> The key activities would include
                        <ol type="a" class="ordered-list-padding">
                            <li>Isolation and characterization of anti-microbial, anti-viral, anti-cancer agents from indigenous medicinal plants,</li>
                            <li>Development of standardized and safe herbal products,</li>
                            <li>R & D and manufacturing of health care food products and functional skin care products,</li>
                            <li>Development of high value therapeutic agents.</li>
                        </ol>
                    </li>
                    <li><strong>Bio-resource based industries for banana, pine apple, citrus etc :</strong> The activities in Bio- resource based industries would include
                        <ol type="a" class="ordered-list-padding">
                            <li>Production of processed food items of banana, pine apple, citrus and other indigenous fruits with enhanced shelf life,</li>
                            <li>Value addition and production of nutraceuticals and dietary supplements from
                                banana, pine apple, citrus and other indigenous fruits,</li>
                            <li>Production of candied pineapple, pineapple juice, dried pineapple etc.</li>
                        </ol>
                    </li>
                    <li><strong>Microbial Biotechnology :</strong> The key activities in microbial biotechnology would include
                        <ol type="a" class="ordered-list-padding">
                            <li>Characterization, utilization and propagation of microbial resources including microbes related to production and processing of fermented food products,</li>
                            <li>Fermentation technology,</li>
                            <li>Enzyme technology and</li>
                            <li>Incorporation of microbes into traditional health beverages.</li>
                        </ol>
                    </li>
                    <li><strong>Processed Foods :</strong> The key activities in food processing would include
                        <ol type="a" class="ordered-list-padding">
                            <li>Molecular characterization and value addition of food products of plant and animal origin,</li>
                            <li>Improvement of food value and shelf life,</li>
                            <li>Quality assessment and value addition in traditional processed foods, and</li>
                            <li>Production and development of ediblecolour,</li>
                            <li>Food products from exotic fruits and vegetables </li>
                            <li>Value addition to tea based liquor</li>
                            <li>Ready- to- drink health beverages</li>
                            <li>Ready-to-eat food based on fermented rice and</li>
                            <li>Silk-pupae (insect) based food items and their preservation.</li>
                        </ol>
                    </li>
                    <li><strong>Seri Biotechnology :</strong> The key activities in Seri biotechnology would include
                        <ol type="a" class="ordered-list-padding">
                            <li>Development of new/superior varieties of host plants having more nutritional properties, more foliage production, resistant to pests and diseases </li>
                            <li>Molecular characterization of species and varieties of silkworm strains,</li>
                            <li>Improvement of silk quality using biotechnological tools,</li>
                            <li>Development of diagnostic kits for important diseases in the larvae,</li>
                            <li>Genetic manipulation of silk protein genes for increased production of silk.</li>
                        </ol>
                    </li>
                    <li><strong>Petroleum Biotechnology :</strong> The key activities in petroleum biotechnology will include
                        <ol type="a" class="ordered-list-padding">
                            <li>Microbial applicationin hydrocarbon prospecting,</li>
                            <li>Development of biotechnology based solutions for exploration of Oil and
                                Gas,</li>
                            <li>Petroleum bio-refining: the selective removal of sulfur, nitrogen, and metals,</li>
                            <li>Bacterial hydrocarbon biosynthesis,/li>
                            <li>Potential applications of bio-surfactants and bio-emulsifiers in the oil industry and</li>
                            <li>Microbial 13 based remediation of hydrocarbon contamination of soil and water; and all other activities through modern and biotechnology approach in the petroleum sector.</li>
                        </ol>
                    </li>
                    <li><strong>Medical Biotechnology :</strong> The key activities in medical biotechnology would be
                        <ol type="a" class="ordered-list-padding">
                            <li>Development of prognostic and diagnostic kits and facilities,</li>
                            <li>Development and commercialization of ELISA and nucleic acid based
                                diagnostics,</li>
                            <li>Methods to develop and use stem cell technology for therapeutic research and tissue regeneration for medical use,</li>
                            <li>Undertaking reproductive health and contraceptive research,/li>
                            <li>Developing strategies for prevention and cure of diseases induced by faulty diet and lifestyle like diabetes, heart disease and obesity,</li>
                            <li>Enzyme biotechnology and</li>
                            <li>R & D activities on woman health.</li>
                        </ol>
                    </li>
                    <li><strong>Bio-energy and Environmental Biotechnology : </strong> The key activities in bio-energy and environmental bio-technology would be
                        <ol type="a" class="ordered-list-padding">
                            <li>Conversion of cellulose and agricultural wastes to produce fuels such as ethanol and butanol by active enzyme systems for their efficient degradation and subsequent fermentation</li>
                            <li>Developing bio-indicators / bio-sensors for pollution control,</li>
                            <li>Development of technologies for treatment of domestic,
                                industrial and medical waste,</li>
                            <li>Identification and development of bio-fuels and bio-energy sources, and</li>
                            <li>Bioremediation technologies.</li>
                        </ol>
                    </li>
                    <li><strong>Tea Biotechnology : </strong> The key activities would include
                        <ol type="a" class="ordered-list-padding">
                            <li>Development of high-yielding organic cultivation,</li>
                            <li>Selection and characterization of water logging resistant cultivars of tea,</li>
                            <li>Bio-control strategies and formulation to combat pest and pathogen infestation,</li>
                            <li>Value addition and product improvement.
                        </ol>
                    </li>
                </ol>
