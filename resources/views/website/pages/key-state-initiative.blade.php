                <h2 class="title-pattern1 mt-0 mb-30"><img src="./icons/planting.png" alt="" width="24"> Key State Initiatives Under The Policy</h2>
                <p>In line with the policy objectives and in pursuance of the strategies mentioned, the following key initiatives shall be undertaken under this policy.</p>
                <h4 class="title-pattern1 mt-30 mb-30">Developing Biotechnology Infrastructure</h4>
                <p>The State Government would promote and facilitate both the public and the private sectors in developing essential high quality infrastructure such as Biotechnology Parks, Research and training facilities and other supporting infrastructure for production, testing, accreditation etc. There will be collaborative partnerships and provisions of incentives/concessions between the state and the private partners. The type and quality of the infrastructure will be chosen considering different agro-climatic, agro-economic and other socio-economic parameters specific for a region.
                </p>
                <h4 class="title-pattern1 mt-30 mb-30">Biotechnology parks and Incubation Centers</h4>
                <p>The State Government proposes to facilitate and support development of general and sector specific biotechnology parks and incubation Centers. These shall be operative as per the following system:</p>
                <ol class="ordered-list-padding" type="A">
                    <li>These will act as an instrument to aggregate Biotechnology industries.</li>
                    <li>These may be a joint-venture of industries, academia and government with clearly specified responsibilities for each.</li>
                    <li>These will be encouraged to operate as self-sustaining organizations for business support, business promotions, enabling start ups and technology support in biotechnology.</li>
                    <li>In line with the state resource potential, the focus of the Biotechnology parks and incubation centers will be as per the thrust areas.</li>
                    <li>The park and the centers will contain industrial, commercial and R & D infrastructure facilities for users.</li>
                    <li>It will act as a technology provider to the small scale biotechnology units and also provide marketing support to the users.</li>
                    <li>The incubation centers shall have all necessary infrastructures and equipment (both specific and shared) for students, researchers and start ups and also provide training and capacity buildings and academic-industry linkages</li>
                </ol>
                <h4 class="title-pattern1 mt-30 mb-30">Developing Academic and Research Infrastructure Facilities</h4>
                    <p>The State government would facilitate setting up laboratory facilities primarily in colleges and universities for human resource development and capacity building. This will ensure an environment conducive for teaching and R&D.</p>
